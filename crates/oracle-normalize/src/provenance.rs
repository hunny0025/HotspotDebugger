//! # Provenance Chain Validator
//!
//! Validates the integrity of the evidence provenance chain from raw artifact
//! through parsing to normalization. Every normalized record must trace back
//! to a specific parsed record, which must trace back to exact bytes in a
//! raw artifact. If any link in this chain is broken, the evidence may be
//! challenged in court.
//!
//! # Validation Rules
//!
//! 1. **Source Reference Completeness:** Every record must have a non-empty
//!    `artifact_id`, `artifact_hash`, `parser_id`, and `parser_version`.
//! 2. **Hash Consistency:** The `artifact_hash` in the source reference must
//!    match the hash of the actual artifact in the evidence store.
//! 3. **Temporal Ordering:** Parsing timestamps must be after acquisition
//!    timestamps, and normalization timestamps must be after parsing timestamps.
//! 4. **Layer Progression:** Records must progress Raw → Parsed → Normalized.
//!    A normalized record cannot reference raw layer directly (must go through parsed).
//! 5. **Parser Version Consistency:** All records from the same parser invocation
//!    must report the same parser version.
//!
//! # Forensic Significance
//!
//! The provenance chain is the backbone of evidence admissibility. Without it,
//! opposing counsel can argue that derived findings have no verifiable connection
//! to the original device data. Every court-ready report must include a provenance
//! chain attestation generated by this validator.

use chrono::{DateTime, Utc};
