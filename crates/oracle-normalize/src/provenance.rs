use serde::{Deserialize, Serialize};
use uuid::Uuid;

// ──────────────────────────────────────────────────────────────────────────────
// Validation Types
// ──────────────────────────────────────────────────────────────────────────────

/// Unique identifier for a validation run.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub struct ValidationId(pub Uuid);

impl ValidationId {
    pub fn new() -> Self {
        ValidationId(Uuid::new_v4())
    }
}

impl Default for ValidationId {
    fn default() -> Self {
        Self::new()
    }
}

impl std::fmt::Display for ValidationId {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "{}", self.0)
    }
}

/// The result of a single provenance chain validation check.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub enum ValidationResult {
    /// The check passed.
    Pass,
    /// The check failed with a reason.
    Fail(String),
    /// The check could not be performed (missing data).
    Skipped(String),
}

impl ValidationResult {
    pub fn is_pass(&self) -> bool {
        matches!(self, ValidationResult::Pass)
    }

    pub fn is_fail(&self) -> bool {
        matches!(self, ValidationResult::Fail(_))
    }
}

impl std::fmt::Display for ValidationResult {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            ValidationResult::Pass => write!(f, "PASS"),
            ValidationResult::Fail(reason) => write!(f, "FAIL: {}", reason),
            ValidationResult::Skipped(reason) => write!(f, "SKIPPED: {}", reason),
        }
    }
}

/// A single provenance chain link being validated.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ProvenanceLink {
    /// The record whose provenance is being validated.
    pub record_id: RecordId,
    /// The evidence layer of this record.
    pub layer: EvidenceLayer,
    /// The source reference claimed by this record.
    pub source_ref: SourceReference,
    /// Optional: the evidence layer of the parent record.
    pub parent_layer: Option<EvidenceLayer>,
}

/// A single validation finding.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ValidationFinding {
    /// Which check produced this finding.
    pub check_name: String,
    /// The record being validated.
    pub record_id: RecordId,
    /// The artifact referenced by the record.
    pub artifact_id: ArtifactId,
    /// The result of the check.
    pub result: ValidationResult,
}

