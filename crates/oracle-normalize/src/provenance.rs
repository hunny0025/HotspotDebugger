    fn make_source_ref(hash: &str, parser: &str, version: &str) -> SourceReference {
        SourceReference {
            artifact_id: ArtifactId::new(),
            artifact_hash: hash.to_string(),
            parser_id: parser.to_string(),
            parser_version: version.to_string(),
            byte_offset: Some(0),
            byte_length: Some(1024),
            db_row_id: None,
            parsed_at: Utc::now(),
        }
    }

    fn make_link(
        layer: EvidenceLayer,
        parent: Option<EvidenceLayer>,
        source_ref: SourceReference,
    ) -> ProvenanceLink {
        ProvenanceLink {
            record_id: RecordId::new(),
            layer,
            source_ref,
            parent_layer: parent,
        }
    }

    // ── Completeness Tests ──────────────────────────────────────────────

    #[test]
    fn test_complete_source_reference_passes() {
        let mut validator = ProvenanceValidator::new();
        let src = make_source_ref("abc123", "wpa_parser", "1.0.0");
        let link = make_link(EvidenceLayer::Parsed, Some(EvidenceLayer::Raw), src);

        validator.validate_link(&link);

        let completeness = validator
            .findings()
            .iter()
            .find(|f| f.check_name == "source_reference_completeness")
            .unwrap();
        assert!(completeness.result.is_pass());
    }

