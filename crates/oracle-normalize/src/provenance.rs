// Validation Report
// ──────────────────────────────────────────────────────────────────────────────

/// Complete provenance chain validation report for an investigation.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ProvenanceReport {
    /// Unique identifier for this validation run.
    pub validation_id: ValidationId,
    /// All individual findings.
    pub findings: Vec<ValidationFinding>,
    /// Overall validation status.
    pub overall_result: ValidationResult,
    /// Summary statistics.
    pub summary: ProvenanceSummary,
    /// When this validation was performed.
    pub validated_at: DateTime<Utc>,
}

/// Summary statistics for a provenance validation.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ProvenanceSummary {
    pub total_checks: usize,
    pub passed: usize,
    pub failed: usize,
    pub skipped: usize,
    pub records_validated: usize,
}

// ──────────────────────────────────────────────────────────────────────────────
// Provenance Chain Validator
// ──────────────────────────────────────────────────────────────────────────────

/// Validates the integrity of evidence provenance chains.
///
/// The validator receives provenance links and known artifact hashes,
/// then executes a series of checks to verify the chain is unbroken.
pub struct ProvenanceValidator {
    findings: Vec<ValidationFinding>,
    /// Known artifact hashes: artifact_id → sha256 hash from the evidence store.
    known_hashes: std::collections::HashMap<String, String>,
    /// Known artifact acquisition timestamps.
    acquisition_times: std::collections::HashMap<String, DateTime<Utc>>,
}

impl ProvenanceValidator {
    /// Create a new provenance validator.
    pub fn new() -> Self {
        ProvenanceValidator {
            findings: Vec::new(),
            known_hashes: std::collections::HashMap::new(),
            acquisition_times: std::collections::HashMap::new(),
        }
    }

    /// Register a known artifact hash from the evidence store.
    ///
    /// This is used to cross-reference source references against
    /// the actual stored artifact hashes.
    pub fn register_artifact_hash(&mut self, artifact_id: &ArtifactId, hash: &str) {
        self.known_hashes
            .insert(artifact_id.to_string(), hash.to_string());
    }

    /// Register a known artifact acquisition time.
    pub fn register_acquisition_time(
        &mut self,
        artifact_id: &ArtifactId,
        acquired_at: DateTime<Utc>,
    ) {
        self.acquisition_times
            .insert(artifact_id.to_string(), acquired_at);
    }

    /// Validate a single provenance link.
    ///
    /// This runs all applicable checks against the provided link:
    /// - Source reference completeness
    /// - Hash consistency (if known)
    /// - Temporal ordering (if acquisition time is known)
    /// - Layer progression
    pub fn validate_link(&mut self, link: &ProvenanceLink) {
        self.check_source_reference_completeness(link);
        self.check_hash_consistency(link);
        self.check_temporal_ordering(link);
        self.check_layer_progression(link);
    }

    /// Generate the final provenance validation report.
    pub fn generate_report(self) -> ProvenanceReport {
        let total = self.findings.len();
        let passed = self.findings.iter().filter(|f| f.result.is_pass()).count();
        let failed = self.findings.iter().filter(|f| f.result.is_fail()).count();
        let skipped = total - passed - failed;

        let records_validated: usize = {
            let mut seen = std::collections::HashSet::new();
            for f in &self.findings {
                seen.insert(f.record_id.0);
            }
            seen.len()
        };

        let overall = if failed > 0 {
            ValidationResult::Fail(format!("{} check(s) failed", failed))
        } else {
            ValidationResult::Pass
        };

        ProvenanceReport {
            validation_id: ValidationId::new(),
            findings: self.findings,
            overall_result: overall,
            summary: ProvenanceSummary {
                total_checks: total,
                passed,
                failed,
                skipped,
                records_validated,
            },
            validated_at: Utc::now(),
        }
    }

    /// Return a reference to all current findings.
    pub fn findings(&self) -> &[ValidationFinding] {
        &self.findings
    }

    // ── Individual Checks ───────────────────────────────────────────────

    /// Check 1: Source reference has all required fields populated.
    fn check_source_reference_completeness(&mut self, link: &ProvenanceLink) {
        let ref_complete = !link.source_ref.artifact_hash.is_empty()
            && !link.source_ref.parser_id.is_empty()
            && !link.source_ref.parser_version.is_empty();

        let result = if ref_complete {
            ValidationResult::Pass
        } else {
            let mut missing = Vec::new();
            if link.source_ref.artifact_hash.is_empty() {
                missing.push("artifact_hash");
            }
            if link.source_ref.parser_id.is_empty() {
                missing.push("parser_id");
            }
            if link.source_ref.parser_version.is_empty() {
                missing.push("parser_version");
            }
            ValidationResult::Fail(format!(
                "Source reference is incomplete — missing: {}",
                missing.join(", ")
            ))
        };

        self.findings.push(ValidationFinding {
            check_name: "source_reference_completeness".to_string(),
            record_id: link.record_id,
            artifact_id: link.source_ref.artifact_id,
            result,
        });
    }

    /// Check 2: Artifact hash in source reference matches the evidence store.
    fn check_hash_consistency(&mut self, link: &ProvenanceLink) {
        let artifact_key = link.source_ref.artifact_id.to_string();

        let result = match self.known_hashes.get(&artifact_key) {
            Some(stored_hash) => {
                if *stored_hash == link.source_ref.artifact_hash {
                    ValidationResult::Pass
                } else {
                    ValidationResult::Fail(format!(
                        "Hash mismatch: source reference claims {} but evidence store has {}",
                        &link.source_ref.artifact_hash[..16.min(link.source_ref.artifact_hash.len())],
                        &stored_hash[..16.min(stored_hash.len())]
                    ))
                }
            }
            None => ValidationResult::Skipped(format!(
                "Artifact {} not found in known hash registry",
                artifact_key
            )),
        };

        self.findings.push(ValidationFinding {
            check_name: "hash_consistency".to_string(),
            record_id: link.record_id,
            artifact_id: link.source_ref.artifact_id,
            result,
        });
    }

    /// Check 3: Parsing timestamp is after acquisition timestamp.
    fn check_temporal_ordering(&mut self, link: &ProvenanceLink) {
        let artifact_key = link.source_ref.artifact_id.to_string();

        let result = match self.acquisition_times.get(&artifact_key) {
            Some(acq_time) => {
                if link.source_ref.parsed_at >= *acq_time {
                    ValidationResult::Pass
                } else {
                    ValidationResult::Fail(format!(
                        "Parsing timestamp ({}) is before acquisition timestamp ({}). \
                         This violates temporal causality and suggests data corruption.",
                        link.source_ref.parsed_at.format("%Y-%m-%dT%H:%M:%SZ"),
                        acq_time.format("%Y-%m-%dT%H:%M:%SZ")
                    ))
                }
            }
            None => ValidationResult::Skipped(format!(
                "Acquisition time for artifact {} not registered",
                artifact_key
            )),
        };

        self.findings.push(ValidationFinding {
            check_name: "temporal_ordering".to_string(),
            record_id: link.record_id,
            artifact_id: link.source_ref.artifact_id,
            result,
        });
    }

    /// Check 4: Evidence layer progression is valid.
    ///
    /// Valid progressions: Raw → Parsed, Parsed → Normalized, Normalized → Correlated.
    /// Invalid: skipping layers (e.g., Raw → Normalized) or backwards (Parsed → Raw).
    fn check_layer_progression(&mut self, link: &ProvenanceLink) {
        let result = match link.parent_layer {
            Some(parent) => {
                if Self::is_valid_progression(parent, link.layer) {
                    ValidationResult::Pass
                } else {
                    ValidationResult::Fail(format!(
                        "Invalid layer progression: {:?} → {:?}. \
                         Records must progress Raw → Parsed → Normalized → Correlated.",
                        parent, link.layer
                    ))
                }
            }
            None => {
                // No parent layer — valid only if this is Raw or Parsed (initial ingestion)
                if link.layer == EvidenceLayer::Raw || link.layer == EvidenceLayer::Parsed {
                    ValidationResult::Pass
                } else {
                    ValidationResult::Fail(format!(
                        "Record at layer {:?} has no parent layer reference. \
                         Only Raw and Parsed layers may lack a parent.",
                        link.layer
                    ))
                }
            }
        };

        self.findings.push(ValidationFinding {
            check_name: "layer_progression".to_string(),
            record_id: link.record_id,
            artifact_id: link.source_ref.artifact_id,
            result,
        });
    }

    /// Determine if a layer progression is valid.
    fn is_valid_progression(from: EvidenceLayer, to: EvidenceLayer) -> bool {
        matches!(
            (from, to),
            (EvidenceLayer::Raw, EvidenceLayer::Parsed)
                | (EvidenceLayer::Parsed, EvidenceLayer::Normalized)
                | (EvidenceLayer::Normalized, EvidenceLayer::Correlated)
        )
    }
}

impl Default for ProvenanceValidator {
    fn default() -> Self {
        Self::new()
    }
}

#[cfg(test)]
